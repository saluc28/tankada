"""
gen_demo_tokens.py — Operator helper. Run on the VPS that hosts the
session-aware Tankada instance (the one serving demo.tankada.io).

Reads JWT_SECRET from the prod environment (or from a path passed via
--env-file) and produces a jwt_tokens.json with two long-lived tokens
(analyst + admin) for the experiment scripts to use.

Usage on the VPS:
    cd /opt/tankada/experiments  # if cloned there; otherwise wherever this lives
    python gen_demo_tokens.py --env-file /opt/tankada/deploy/.env \\
                              --tenant tenant_1 \\
                              --expiry-days 365 \\
                              --output jwt_tokens.json

The output file should be committed to the public repo so reviewers can
reproduce the experiment without contacting the maintainers. Tokens are
read-only and rate-limited. If they ever leak in an unintended way,
rotate JWT_SECRET on the prod env and regenerate.
"""

from __future__ import annotations

import argparse
import json
import sys
import time
from pathlib import Path

import jwt as pyjwt


def parse_env_file(path: Path) -> dict[str, str]:
    env: dict[str, str] = {}
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        if "=" not in line:
            continue
        k, _, v = line.partition("=")
        env[k.strip()] = v.strip()
    return env


def make_token(secret: str, role: str, tenant: str, expiry_days: int) -> str:
    exp = int(time.time()) + expiry_days * 86400
    if role == "analyst":
        payload = {
            "agent_id":      f"experiment-analyst-{tenant}",
            "owner_user_id": "experiment",
            "tenant_id":     tenant,
            "roles":         ["analyst"],
            "dataActions": [
                f"{tenant}/financial/accounts/read",
                f"{tenant}/financial/transactions/read",
                f"{tenant}/financial/cards/read",
                f"{tenant}/financial/loans/read",
            ],
            "notDataActions": [],
            "exp":              exp,
        }
    elif role == "admin":
        payload = {
            "agent_id":      f"experiment-admin-{tenant}",
            "owner_user_id": "experiment",
            "tenant_id":     tenant,
            "roles":         ["admin"],
            "dataActions":   [f"{tenant}/*/*/read"],
            "notDataActions": [],
            "exp":              exp,
        }
    else:
        raise ValueError(f"unknown role: {role}")
    return pyjwt.encode(payload, secret, algorithm="HS256")


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate long-lived JWTs for the demo experiment scripts.")
    parser.add_argument("--env-file", type=Path, required=True, help="Path to the prod .env containing JWT_SECRET.")
    parser.add_argument("--tenant", default="tenant_1", help="Tenant id to embed in tokens. Default: tenant_1")
    parser.add_argument("--expiry-days", type=int, default=365, help="Token expiry in days. Default: 365")
    parser.add_argument("--output", type=Path, default=Path("jwt_tokens.json"), help="Output path. Default: jwt_tokens.json")
    args = parser.parse_args()

    if not args.env_file.exists():
        print(f"env file not found: {args.env_file}", file=sys.stderr)
        return 1

    env = parse_env_file(args.env_file)
    secret = env.get("JWT_SECRET")
    if not secret or secret == "dev-secret-change-in-production":
        print("JWT_SECRET missing or set to dev default. Refusing to generate tokens.", file=sys.stderr)
        return 2

    tokens = {
        "_comment": (
            f"Long-lived demo tokens for the experiment scripts. "
            f"Generated by gen_demo_tokens.py on {time.strftime('%Y-%m-%d')}. "
            f"Tenant {args.tenant}, expiry {args.expiry_days} days. "
            f"Rotate by re-running this script and committing the new file."
        ),
        "analyst": make_token(secret, "analyst", args.tenant, args.expiry_days),
        "admin":   make_token(secret, "admin",   args.tenant, args.expiry_days),
    }
    args.output.write_text(json.dumps(tokens, indent=2) + "\n")
    print(f"wrote {args.output} with analyst + admin tokens (expiry {args.expiry_days} days, tenant {args.tenant})")
    return 0


if __name__ == "__main__":
    sys.exit(main())
